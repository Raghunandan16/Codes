void solve()
{
    int n, m;
    cin >> n >> m;
    string s;
    cin >> s;
    vector<int> pre(n);
    pre[0] = (s[0] == '1');
    for (int i = 1; i < n; i++)
    {
        pre[i] = pre[i - 1] + (s[i] == '1');
        /* debug(pre[i]); */
    }
    vector<int> one, zero;
    for (int i = 0; i < n; i++)
    {
        if (s[i] == '1')
            one.pb(i);
        else
            zero.pb(i);
    }
    set<pii> st;
    while (m--)
    {
        int l, r;
        cin >> l >> r;
        --l, --r;
        /*  debug(l, r);
         debug(pre[r], pre[l]); */
        int count = pre[r];
        if (l > 0)
            count -= pre[l - 1];
        if (l == r || count == 0 || count == r - l + 1)
        {
            st.insert({-1, -1});
            /* debug(count); */
            continue;
        }
        int lzero = upper_bound(all(zero), r) - zero.begin() - 1;
        lzero = zero[lzero];
        int fone = lower_bound(all(one), l) - one.begin();
        fone = one[fone];
        if (lzero > fone)
            st.insert({lzero, fone});
        else
            st.insert({-1, -1});
    }
    cout << st.size() << "\n";
}

